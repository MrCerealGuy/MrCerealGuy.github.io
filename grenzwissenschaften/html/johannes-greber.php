											<p><b><font face="Arial"><a name="2"></a>2.&#9;Die Wende im Leben von Johannes Greber</font></b>
											<p>In Koblenz kam nun im Sp&auml;tsommer 1923 ein Mann zu Greber und fragte ihn 
											  nach seiner Ansicht &uuml;ber den Spiritualismus.&nbsp;(<font face="Times New Roman, Times, serif"><a href="" onClick="einleitung2f1=window.open('einleitung2f1.htm', 'win1','toolbar=no,location=no,directories=no,status=no,menubar=no,scrollbars=yes,resizable=yes,copyhistory=yes,width=400,height=300,top=40,left=150'); return false;">-1-</a></font>) 
											  Zugleich forderte er ihn auf, sich sein Urteil nach eigener Anschauung zu bilden. 
											  Der Mann berichtete, da&szlig; er w&ouml;chentlich mit einem kleinen Kreis von 
											  Menschen zu einer Art Gottesdienst zusammenkomme. Dabei werde gebetet, in der 
											  Bibel gelesen und das Gelesene hinterher besprochen. Unter den Anwesenden befinde 
											  sich auch ein Junge im Alter von 16-17 Jahren (sein Name war Erich Zimmermann). 
											  Er habe nur eine mittelm&auml;&szlig;ige Schulbildung und sei Lehrling in einem 
											  Privatbetrieb. Bei den Zusammenk&uuml;nften pflege dieser, pl&ouml;tzlich bewu&szlig;tlos 
											  vorn&uuml;ber zu fallen, werde dann aber wie von einer unsichtbaren Kraft ruckweise 
											  wieder aufgerichtet. Dann sitze er mit geschlossenen Augen da und erteile den 
											  Anwesenden wunderbare Belehrungen. Auch beantworte er Fragen, die man an ihn 
											  stelle. Am Schlu&szlig; des Vorganges komme er wieder zum Bewu&szlig;tsein, 
											  k&ouml;nne sich aber an das Vorgegangene in keiner Weise erinnern. 
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Nach anf&auml;nglichem Z&ouml;gern, weil er eine Falle bef&uuml;rchtete, 
											  nahm Greber am folgenden Sonntagabend an einer solchen Zusammenkunft teil. Zur 
											  Vorbereitung hatte er sich auf einem Zettel einige Fragen aufgeschrieben, die 
											  er an den zu erwartenden Geist stellen wollte. Nach einem einleitenden Gebet, 
											  und nachdem der Junge in Trance gefallen war, sprach der Geist Greber auf seinen 
											  in der Tasche befindlichen Fragezettel hin an. Verbl&uuml;fft zog Greber seinen 
											  Fragezettel heraus und stellte als erste Frage (1, S. 17): </p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"Wie kommt es, da&szlig; das Christentum 
											      auf die heutige Menschheit fast keinen Einflu&szlig; mehr auszu&uuml;ben 
											      scheint?"</font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<p align="JUSTIFY">Die Antwort, die Greber gleich stenographisch aufzeichnete, 
											  lautete: </p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"Die Lehre Christi ist in den auf euch gekommenen 
											      Urkunden nicht mehr in ihrem ganzen Umfang und auch nicht in ihrer urspr&uuml;nglichen 
											      Reinheit und Klarheit enthalten. In dem, was ihr Neues Testament nennt, 
											      sind manche wichtigen Abschnitte weggelassen. Ja, ganze Kapitel wurden daraus 
											      entfernt. Was ihr noch besitzt, sind verst&uuml;mmelte Abschriften. Die 
											      Originale sind euch unbekannt, so da&szlig; die Verst&uuml;mmlungen des 
											      Urtextes nicht aufgedeckt werden k&ouml;nnen. Die dieses getan haben, sind 
											      von Gott schwer bestraft worden. </font></p>
											    <p align="JUSTIFY"></p>
											    <p align="JUSTIFY"><font size="2">Wenn ihr den vollst&auml;ndigen und unverf&auml;lschten 
											      Text der Lehre Christ h&auml;ttet, w&uuml;rde euch so manche Last von den 
											      Schultern genommen sein, die Euch von Menschen im Namen der Religion und 
											      des Christentums aufgeb&uuml;rdet wurde. Manche Lehre, die man euch zu glauben 
											      zumutet, obschon sie eurem Verstande unm&ouml;glich erscheint, w&uuml;rde 
											      in Wegfall kommen, weil sie als unrichtig erkannt w&uuml;rde, und ihr k&ouml;nntet 
											      aufatmen als freie Kinder Gottes. </font></p>
											    <p align="JUSTIFY"></p>
											    <p align="JUSTIFY"><font size="2">So aber f&uuml;hlen Millionen Menschen, 
											      da&szlig; vieles von dem, was heute das Christentum lehrt, nicht richtig 
											      sein kann. Aus Gewohnheit behalten sie es zwar &auml;u&szlig;erlich bei. 
											      Aber eine innere Wirkung hat es nicht. Denn das lebendige F&uuml;rwahrhalten 
											      fehlt. Viele behalten das heutige Christentum aber nicht einmal &auml;u&szlig;erlich 
											      bei. Anstatt nur das Unrichtige daran abzuwerfen, sch&uuml;tteln sie <i>das 
											      ganze</i> Christentum samt dem Gottesglauben ab, weil sie meinen, das h&auml;nge 
											      alles zusammen. Und das ist schlimm. - Doch wird die Zeit kommen, wo die 
											      Lehre Christi in ihrer vollen Reinheit und Wahrheit der Menschheit wiedergegeben 
											      wird. Auf welche Weise das geschieht, braucht ihr jetzt noch nicht zu wissen."</font></p>
											    <p align="JUSTIFY"></p>
											    <p align="JUSTIFY">&nbsp;</p>
											  </dir>
											</dir>
											<p align="JUSTIFY">Nach weiteren l&auml;ngeren Ausf&uuml;hrungen bestellte das 
											  Geistwesen durch den Mund des medialen Jungen Johannes Greber f&uuml;r den n&auml;chsten 
											  Abend zu einer weiteren Aussprache, dann aber nur unter vier Augen.</p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Nach einer ziemlich schlaflosen Nacht ging Greber am n&auml;chsten 
											  Abend erneut in die Wohnung, in der er am Vortag war. Um 19.30 Uhr fiel der 
											  mediale Junge wieder in Trance, und das eingetretene Geistwesen begr&uuml;&szlig;te 
											  Johannes Greber mit gro&szlig;er Freundlichkeit. Es sagte (1, S. 22):</p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"Nun sprich dich ganz offen und vertrauensvoll 
											      bei mir aus, denn ich wei&szlig;, da&szlig; seit gestern in deinem Inneren 
											      alles drunter und dr&uuml;ber geht, und du dich nicht mehr zurechtfindest."</font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<p align="JUSTIFY">Greber erwiderte mit vor innerer Erregung bebender Stimme:</p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"Meine Gedanken wirbeln durcheinander. Ich 
											      wei&szlig; nicht, was ich von allem halten soll. Ich bitte dich, belehre 
											      mich &uuml;ber alles, und sage mir vor allem, wer du bist und wie es m&ouml;glich 
											      ist, da&szlig; du durch diesen Jungen redest."</font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<p align="JUSTIFY">Die Antwort lautete:</p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"Du hast recht, da&szlig; du mich zun&auml;chst 
											      fragst<i>, wer</i> ich bin. Denn ihr sollt die Geister, die zu euch reden, 
											      vor alle<i>m pr&uuml;fen, ob sie von Gott sind</i>, damit ihr nicht die 
											      Opfer b&ouml;ser Geister werdet, die euch lieblich und geistig zugrunde 
											      richten, euch nicht die Wahrheit sagen, sondern die L&uuml;ge, und dadurch 
											      euren Lebensweg zum Abgrund f&uuml;hren. – Ich schw&ouml;re es dir bei Gott, 
											      da&szlig; ich ein guter Geist Gottes bin, und zwar einer der h&ouml;chsten. 
											      Meinen Namen behalte f&uuml;r dich!"</font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<p align="JUSTIFY">Nun nannte er seinen Namen.</p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"Ich bin es, der dich hierher gef&uuml;hrt 
											      hat. Ich will dich im Auftrage Gottes lehren, und du hinwiederum lehre deine 
											      Mitmenschen!"</font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<p align="JUSTIFY">Im Verlauf der weiteren Unterhaltung wurde Greber aufgefordert, 
											  jeden Sonntagabend um 20.00 Uhr wieder in die Wohnung in Koblenz zu kommen, 
											  um weitere Belehrungen zu erhalten, und au&szlig;erdem in seiner Gemeinde in 
											  Kell ebenfalls einen medialen Entwicklungskreis zu bilden. Abschlie&szlig;end 
											  sagte das Geistwesen zu ihm (1, S.25):</p>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">"F&uuml;r heute m&ouml;gen diese allgemeinen 
											      Belehrungen gen&uuml;gen. &Uuml;ber die Einzelheiten des Verkehrs wirst 
											      du im Laufe der Zeit genauer unterrichtet werden, wenn du willens bist, 
											      dich belehren zu lassen und die Aufgabe zu &uuml;bernehmen, die dir zugedacht 
											      ist. Gezwungen wirst du nicht. Du hast deinen freien Willen. Du kannst das 
