											<p><b><font face="Arial"><a name="1"></a>1.&#9;Johannes Grebers Leben als katholischer Priester 
											  in Deutschland</font></b>
											<p align="JUSTIFY">&nbsp;</p>
											<p align="JUSTIFY">Johannes Greber wurde am 2. Mai 1874 in Wenigerath, Kreis Bernkastel 
											  geboren. Grebers Eltern waren arme Kleinbauern. Weil der Sohn aber au&szlig;ergew&ouml;hnlich 
											  begabt war, unterst&uuml;tzte ihn der Dorfpfarrer finanziell und schickte ihn 
											  auf das Konvikt in Pr&uuml;m (80 km s&uuml;dwestlich von Koblenz). Nach bestandenem 
											  Abitur trat Greber 1896 in das Priesterseminar in Trier ein und studierte dort 
											  katholische Theologie. Am 31. M&auml;rz 1900 wurde er zum Priester geweiht. 
											  Anschlie&szlig;end war er von 1900 bis 1901 in Saarbr&uuml;cken und von 1901 
											  bis 1904 in Trier als Kaplan t&auml;tig. Am 1. Mai 1904 &uuml;bernahm er seine 
											  erste Pfarrstelle in Obergondershausen (Bild 1) im Hunsr&uuml;ck (17,5 km s&uuml;ds&uuml;dwestlich 
											  von Koblenz), die er bis zum 31. M&auml;rz 1915 innehatte. Er war als Mensch 
											  und Pfarrer sehr beliebt, hatte eine gewaltige Predigergabe und war unerm&uuml;dlich 
											  aktiv in der t&auml;tigen N&auml;chstenliebe. </p>
											<table border="0" cellspacing="0" cellpadding="0" align="center" width="450">
											  <tr>
											    <td width="450"><img src="/grenzwissenschaften/gfx/greber/einleitung1b1.jpg" width="450" height="274"></td>
