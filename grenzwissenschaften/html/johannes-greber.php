											      mit GOTT haltet und alles in SEINEM NAMEN tut, Euch dem SCHUTZE GOTTES unterstellt 
											      und IHN liebt und stets das Gute wollt, so wird euch diese Verbindung mit 
											      der Geisterwelt nie schaden. Ihr werdet im Gegenteil dadurch <i>k&ouml;rperlich 
											      und seelisch gest&auml;rkt</i>. Am meisten jedoch die Medien. Denn der Schlaf, 
											      den ihr zur St&auml;rkung braucht, haben die Medien, die in Tieftrance fallen, 
											      w&auml;hrend dieser Zeit nicht n&ouml;tig. Aber nur, wenn ihr dem Guten 
											      dient und b&ouml;se Geister von euch weist. </font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><i><font size="2">Solange die Medien im Trancezustand sind, ruht ihr 
											        K&ouml;rper und wird dadurch gekr&auml;ftigt. </font></i></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">Wenn wir guten Geister auch an ihnen oder 
											      durch sie arbeiten, so schadet ihnen dies durchaus <i>nicht</i>. Sie ruhen 
											      sich vielmehr, wie gesagt, k&ouml;rperlich aus und f&uuml;hlen sich nach 
											      Schlu&szlig; der Sitzung <i>wohler als vorher</i>. </font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><i><font size="2">Die Odkraft, die den Medien und Teilnehmern einer 
											        Sitzung entzogen wird, ersetzt die gute Geisterwelt durch frisches Od. 
											        </font></i></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">Dazu kommt, da&szlig; bei Ausbildung der 
											      Medien innere Gebrechen des Mediums, die den Trancezustand erschweren oder 
											      verhindern, zuerst durch Eingreifen der guten Geisterwelt beseitigt werden, 
											      so da&szlig; der betreffende Mensch nach seiner Ausbildung als 'Tieftrancemedium' 
											      <i>ges&uuml;nder ist als vorher.</i></font></p>
											    <p align="JUSTIFY"></p>
											    <p align="JUSTIFY"><font size="2">Der Spiritismus <i>schadet jedoch</i>, wenn 
											      ihr GOTT dabei <i>au&szlig;er acht</i> lasset, alles mit dem B&ouml;sen 
											      betreibt, euch sogar an dem B&ouml;sen <i>belustigt</i> und das Gebet <i>verge&szlig;t</i>. 
											      So verfallt ihr nach und nach dem B&ouml;sen. Das ist nicht blo&szlig; deswegen 
											      so schlimm, weil ihr durch die b&ouml;sen Geister vom Weg der WAHRHEIT und 
											      rechten ERKENNTNIS weggelockt werdet, sondern weil sie euch auch <i>k&ouml;rperlich 
											      schwer sch&auml;digen. </i></font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><font size="2"><i>Denn die Odkraft, die sie euch entziehen, wird von 
											        ihnen nicht mehr ersetzt. Infolgedessen wird vor allem die Gesundheit 
											        der Medien, aber auch die der Teilnehmer, sehr geschw&auml;cht und nach 
											        und nach vollst&auml;ndig zugrunde gerichtet. </i></font></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">So ist ein K&ouml;rnchen Wahrheit in dem 
											      Volksglauben, da&szlig; derjenige sein Leben einsetzen m&uuml;sse, der mit 
											      dem Teufel ein B&uuml;ndnis schlie&szlig;t. Denn seine Odkraft wird durch 
											      das B&ouml;se nach und nach verbraucht, und sein irdischer K&ouml;rper bleibt 
											      nicht mehr lebensf&auml;hig. </font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><i><font size="2">Viele Medien, die dem niederen Spiritismus dienen, 
											        erleiden eine geistige und k&ouml;rperliche Zerr&uuml;ttung. Manche enden 
											        im Irrenhaus oder begehen Selbstmord.</font></i></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">Gefahren und Sch&auml;digungen sind also 
											      im Spiritismus nur dort vorhanden, wo man ihn nicht betreibt, um die g&ouml;ttliche 
											      WAHRHEIT kennenzulernen und innerlich <i>besser</i> zu werden, sondern wo 
											      man darin blo&szlig; seine Neugierde, seine Sucht nach au&szlig;erordentlichen 
											      Erlebnissen befriedigen, Ausk&uuml;nfte f&uuml;r sein materielles Fortkommen 
											      erhalten oder rein wissenschaftliche Erkenntnisse gewinnen will. </font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><i><font size="2">Warne daher deine Mitmenschen aufs ernstlichste vor 
											        der Teilnahme an einem Geisterverkehr, der nicht h&ouml;heren Zwecken 
											        dient. </font></i></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">&nbsp;</p>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><i><font size="2">Belehre sie &uuml;ber den guten, gottgewollten SPIRITUALISMUS. 
											        Denn diesen soll jeder Mensch betreiben. Er ist f&uuml;r ihn der einzige 
											        Weg zur WAHRHEIT und der k&uuml;rzeste Weg zu GOTT.</font></i></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">&nbsp;</p>
											<dir> 
											  <dir> 
											    <p align="JUSTIFY"><font size="2">Auch der, welcher sic<i>h noch nicht</i> 
											      zum Gottesglauben durchgerungen hat, soll sich an dem guten Spiritualismus 
											      beteiligen, sofern er den ehrlichen Willen hat, die WAHRHEIT anzunehmen, 
											      sobald sie ihm in &uuml;berzeugender Weise geboten wird. Wahrheitssucher 
											      mit solcher Gesinnung werden auf diesem Wege die WAHRHEIT und die Freiheit 
											      der Kinder GOTTES finden. Sie werden erkennen, worin wahre RELIGION in Wirklichkeit 
											      besteht. Von ihnen gilt das Wort CHRISTI: 'Suchet und ihr werdet finden!'</font></p>
											    <p align="JUSTIFY"></p>
											    <p align="JUSTIFY"><font size="2">Die, welche <i>noch nicht</i> an GOTT glauben, 
											      sollen doch zu GOTT, wenn auch nur bedingungsweise, beten. Sie m&ouml;gen 
											      das Gebet ihrem augenblicklichen Zustand anpassen. Folgendes Gebet kann 
											      jeder Ungl&auml;ubige sprechen, wenn er guten Willens und bereit ist, die 
											      WAHRHEIT anzunehmen: 'O GOTT, wenn es wahr ist, da&szlig; DU existierst, 
											      so bitte ich DICH von Herzen: Lehre mich DICH erkennen! Zeige mir die WAHRHEIT 
											      und f&uuml;hre mich den rechten Weg! – Amen.' Er wird sicher erh&ouml;rt 
											      werden. Denn GOTT erbarmt sich eines jeden, der guten Willen hat. </font></p>
											    <p align="JUSTIFY"></p>
											  </dir>
											</dir>
											<ul>
											  <p align="JUSTIFY">
											  <ul>
											    <ul>
											      <li><i><font size="2">Welcher religi&ouml;sen Gemeinschaft einer angeh&ouml;rt, 
											        ist f&uuml;r die Beteiligung an guten spiritualistischen Zusammenk&uuml;nften 
											        belanglos."</font></i></li>
											    </ul>
											  </ul>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Zu diesen Ausf&uuml;hrungen ist zu sagen, da&szlig; auch beim 
											  besten Willen, bei lautersten Motiven und Einhaltung der hier beschriebenen 
											  Ratschl&auml;ge und Regeln sich <i>trotzdem</i> niedere Geistwesen in den Jenseitsverkehr 
											  einschalten k&ouml;nnen. H&auml;ufig geben sie sich fromm und schm&uuml;cken 
											  sich mit Engel- oder Heiligen Namen oder geben sich als "Gott" oder "Jesus Christus" 
											  aus. Die Schwierigkeit besteht jetzt darin, herauszufinden, wer diese Wesen 
											  wirklich sind. Helfen tut nur, sie schw&ouml;ren zu lassen, "da&szlig; sie zu 
											  Gottes guter Geisterwelt geh&ouml;ren und da&szlig; Jesus Christus sowohl ihr 
											  Herr als auch der Herr &uuml;ber Luzifer den Teufel ist". Dieser letzte Halbsatz 
											  wird von den Niederen in der Regel nicht w&ouml;rtlich nachgesprochen. Darauf 
											  mu&szlig; man aber bestehen und im Weigerungsfall das Geistwesen abweisen. </p>
											<p align="JUSTIFY"></p>
											<ul>
											  <p align="JUSTIFY">
											  <li><i>Leichtfertige Aus&uuml;bung eines Jenseitsverkehrs ohne st&auml;ndiges 
											    Gebet um g&ouml;ttlichen SCHUTZ kann sehr schnell in Besessenheit oder Umsessenheit 
											    umschlagen. </i></li>
											  <p></p>
											</ul>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Der Betroffene h&ouml;rt dann pausenlos Stimmen, die ihn beleidigen, 
											  zu sinnlosen Handlungen n&ouml;tigen und am Schlaf hindern. Solche Menschen 
											  k&ouml;nnen f&uuml;r ihr weiteres Leben Patienten der Psychiatrie werden.</p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Ein Gebet um g&ouml;ttlichen SCHUTZ bei der Aufnahme eines 
											  Jenseitsverkehrs kann z. B. folgenden Wortlauf haben:</p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">&nbsp;</p>
											<p align="JUSTIFY">&nbsp;</p>
											<p align="JUSTIFY">Wir bitten Dich, GOTT DU ALLM&Auml;CHTIGER, uns gute Geister 
											  zu schicken, um uns beizustehen und jene zu entfernen, die DIR nicht dienen 
											  und die uns in einen Irrtum f&uuml;hren k&ouml;nnten. Verleihe uns das n&ouml;tige 
											  LICHT, um die WAHRHEIT vom Betruge unterscheiden zu k&ouml;nnen. </p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Entferne auch die &uuml;belwollenden Geister, die zwischen 
											  uns Uneinigkeit bringen k&ouml;nnten, indem sie Neid, Stolz und die Eifersucht 
											  erwecken. Wenn es einige versuchen sollten, sich hier einzuschleichen, so beschw&ouml;ren 
											  wir sie im NAMEN GOTTES, sich zur&uuml;ckzuziehen. </p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Gute Geister, die Ihr GOTT und CHRISTUS dient, kommet zu uns, 
											  um uns zu unterrichten. Machet uns empf&auml;nglich f&uuml;r Eure Lehren. Machet, 
											  da&szlig; ein jedes pers&ouml;nliche Gef&uuml;hl in uns vor dem Gedanken an 
											  das allgemeine Wohl verschwinde. </p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Wir bitten besonders unsere Schutzgeister, da&szlig; sie uns 
											  ihren Beistand angedeihen lassen. - Amen.</p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">&nbsp;</p>
											<p align="JUSTIFY">Wer Jenseitsverkehr betreibt oder betreiben will, ganz gleich 
											  mit welchem Verfahren, sollte sich die Brosch&uuml;re "<i>Die Zuverl&auml;ssigkeit 
											  medialer Durchgaben und die Pr&uuml;fung der Geister</i>" (5) aus den "Psychowissenschaftlichen 
											  Grenzgebieten" ausdrucken, um zu erkennen, welche raffinierten T&auml;uschungsverfahren 
											  von Jenseitigen oft angewendet werden.</p>
