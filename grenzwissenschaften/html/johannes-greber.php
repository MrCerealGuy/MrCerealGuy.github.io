											<p>&nbsp;</p><p align="JUSTIFY">Die Gegend, wo er sein Amt aus&uuml;bte, war eine der &auml;rmsten 
											  in Deutschland. Viele der Bewohner waren an Tuberkulose erkrankt. An die &Auml;rmsten 
											  im Dorf verteilte er oft von ihm selbst bezahlte Gutscheine, f&uuml;r die sie 
											  sich in den L&auml;den der D&ouml;rfer Grundnahrungsmittel wie Brot oder Fleisch 
											  kaufen konnten. &Auml;rztliche Hilfe blieb den meisten Kranken versagt, da sie 
											  sich diese Hilfe nicht leisten konnten. Weil der n&auml;chste Arzt erst in 20 
											  km Entfernung in Boppard zu erreichen war, befa&szlig;te sich Greber, der gerne 
											  Medizin studiert h&auml;tte, schon in Obergondershausen auch mit der Behandlung 
											  von Krankheiten. Damals mit sog. Hausmitteln und dem Auflegen der H&auml;nde. 
											  Er verwendete Heilkr&auml;uter und Wasseranwendungen nach Kneipp. </p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Greber war ohne Murren zu jeder Tages- und Nachtzeit zur Hilfe 
											  bereit. Das war ihm auch deswegen m&ouml;glich, weil er ein Pferd besa&szlig; 
											  und damit schnell selbst in entlegene D&ouml;rfer seiner Pfarrei gelangen konnte. 
											  Au&szlig;erdem gr&uuml;ndete er einen Hilfsbund zur Pflege und Unterst&uuml;tzung 
											  erkrankter Angeh&ouml;riger seiner und umliegender Kirchengemeinden. Greber 
											  lie&szlig; Krankenschwestern ausbilden, um den Kranken seiner Gemeinde zu helfen. 
											  Als seine gr&ouml;&szlig;te Aufgabe sah er es an, diese Leiden zu lindern und 
											  die Kranken zu besuchen. Zur Begr&uuml;&szlig;ung gab er diesen die Hand und 
											  legte ihnen die andere Hand auf die Stirn, w&auml;hrend er ein stilles Gebet 
											  sagte. Von HEILKRAFT wu&szlig;te er zu dieser Zeit noch nichts. Erst in sp&auml;teren 
											  Jahren in den USA wirkte er in st&auml;rkerem Ma&szlig;e <i>als geistiger Heiler</i>.</p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Im August 1914, als die Glocken zur Mobilmachung l&auml;uteten, 
											  &uuml;berfiel Greber gro&szlig;e Besorgnis und Beunruhigung. Zugleich hatte 
											  er eine Vision: Er sah, wie in einem Film, Tausende halbverhungerte Kinder vor 
											  sich. Ein FINGER deutete auf diese vielen Kinder und eine STIMME sprach zu ihm: 
											  "Das ist Deine Arbeit." - Nach zwei Jahren Weltkrieg, als die gro&szlig;e Hungersnot 
											  hereinbrach, wurde ihm die Bedeutung dieser Vision klar. Nun konnte er die unterern&auml;hrten 
											  Kinder in Wirklichkeit sehen. Er machte sich an die Arbeit, um Abhilfe zu schaffen, 
											  und kam zu dem Entschlu&szlig;, die hungernden Kinder nach Holland zur Erholung 
											  zu bringen. Dieser Plan stie&szlig; jedoch auf gro&szlig;e Schwierigkeiten<i>. 
											  Sein Bischof sowie alle Beh&ouml;rden versagten jegliche Hilfe</i>. Pfarrer 
											  Greber lie&szlig; sich nicht ablenken. Allein machte er sich an die gro&szlig;e 
											  Aufgabe, und organisierte im Rahmen des Hilfsbundes von 1915 bis 1918 die zeitweilige 
											  Unterbringung von unterern&auml;hrten Stadtkindern bei deutschen Bauern auf 
											  dem Land und von 1916 an im benachbarten Holland. In etwa zwei Jahren brachte 
											  er es fertig, <i>mehr als</i> <i>20.000 Kinder</i> bei Pflegeeltern auf dem 
											  Land unterzubringen. Oft gab er seinen letzten Pfennig, um die n&ouml;tigen 
											  Fahrkarten f&uuml;r die Kinder zu kaufen. </p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Nach fast elfj&auml;hriger T&auml;tigkeit als Pfarrer in Obergondershausen 
											  lie&szlig; sich Greber zum 31. M&auml;rz 1915 vom Trierer Bischof Felix Korum 
											  beurlauben, um eine Stelle als Direktor des "Mittelrheinisch-nassauischen Bauernvereins" 
											  anzunehmen. Aus diesem Grund verlegte er seinen Wohnsitz nach Koblenz. Allerdings 
											  &uuml;bte er die T&auml;tigkeit nur bis zum 28. Juli 1916 aus, blieb aber weiterhin 
											  in Koblenz wohnen. Seine Arbeitskraft widmete Greber f&uuml;r die n&auml;chsten 
											  zwei Jahre &uuml;berwiegend dem von ihm begr&uuml;ndeten Hilfsbund. Bei der 
											  Durchf&uuml;hrung dieser Aufgaben hatte Greber gelernt, gr&ouml;&szlig;ere Unternehmungen 
											  zu leiten und sich gegen&uuml;ber Widerst&auml;nden <i>durchzusetzen</i>. Er 
											  hatte gezeigt, da&szlig; er aufopferungsf&auml;hig war, und damit den Beweis 
											  erbracht, auch <i>noch bedeutenderen</i> Auftr&auml;gen gewachsen zu sein. Diese 
											  kamen in der Inflationszeit auf ihn zu. </p>
											<p align="JUSTIFY"></p>
											<p align="JUSTIFY">Als nach dem Kriegsende die Kinderverschickung nicht mehr erforderlich 
											  war, trat Greber wieder in den aktiven Kirchendienst ein und &uuml;bernahm die 
											  Pfarrei der kleinen Gemeinde Kell (Bild 2), 5 km nordwestlich von Andernach. 
											  Auch dort war er ein von seiner Gemeinde angesehener und sehr beliebter Pastor. 
											  Zugleich nahm er auch noch die Leitung des von ihm begr&uuml;ndeten Hilfsbundes 
											  wahr, dessen B&uuml;ro sich in Koblenz befand. Zweimal in der Woche fuhr er 
											  dorthin, um die anfallenden Wohlfahrtsarbeiten zu erledigen.</p>
